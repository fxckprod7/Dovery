# │   ├── css/              # Subdirectory for CSS files
# │   ├── js/               # Subdirectory for JavaScript files
# │   └── images/           # Subdirectory for image files
# │
# ├── templates/            # Directory for HTML templates used by the Flask app
# │   ├── base.html         # Base template for consistent layout across pages
# │   └── index.html        # Main index page template
# │
# ├── instance/             # Instance folder for configuration and sensitive data
# │   └── config.py         # Instance-specific configuration file (e.g., for development or production)
# │
# └── tests/                # Directory for unit tests to ensure code functionality
#     ├── test_app.py       # Test file for the main application
#     └── __init__.py       # Initialization file for the tests package
